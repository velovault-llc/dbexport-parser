// turn raw class IDs and property IDs into human-readable labels.
//
// Provenance:
//   - CLASS_NAMES, PROP_NAMES, PROP_NAMES_BY_CLASS, CLASS_META, and
//     CAF_BUCKET_MAP are sourced from the sibling project
//     jmsboswell67-alt/dbexport-viewer (Apache 2.0), which assembled them
//     from JCI Launcher resource files (commands.xml, globalModifyList.xml,
//     AttributeSetList.xml, AttributeSetListSCT.xml) plus a substantial
//     dictionary contribution from thecontrolscompany/oct (Apache 2.0,
//     maintained by tcollins2). See references/oct-shared/SOURCE.md in
//     dbexport-viewer for the full attribution + reconciliation notes.
//
// Legal posture:
//   These are factual numeric-ID -> canonical-name mappings extracted
//   for interoperability purposes per Feist v. Rural (US),
//   Software Directive Art. 6 (EU), and the Sega / Sony / Connectix
//   line of interop reverse-engineering cases. The original JCI
//   Launcher resource files and the OCT JSON dictionaries are NOT
//   redistributed by this project — only the extracted ID -> name
//   tables are included.
//
// Schema:
//   CLASS_NAMES          : Record<classId,    className>
//   PROP_NAMES           : Record<propId,     propertyName>             // flat fallback
//   PROP_NAMES_BY_CLASS  : Record<classId, Record<propId, propertyName>> // class-specific override
//   CLASS_META           : Record<classId, ClassMeta>                    // category/bacnet/context enrichment
//   CAF_BUCKET_MAP       : Record<classId,    cafWorkspaceBucket>        // CCT 5-column workspace classifier
//   WORKSPACE_HIDDEN_BUCKET                                              // sentinel: hide from CAF workspace UI
//
// Helpers in src/index.ts:
//   classLabel(cid)       -> string
//   propLabel(pid, cid?)  -> string  // consults PROP_NAMES_BY_CLASS first when cid is supplied

